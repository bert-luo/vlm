"""
generate.py — Text prompt → Blender .bpy script → rendered PNG

Default (Anthropic): one agentic trajectory with write_file / render tools (same as
generate_trajectories.py, single prompt).

Legacy single-shot mode: pass --simple, or use --endpoint (OpenAI-compatible).

Usage:
    uv run eval/generate.py "high school student study room"
    uv run eval/generate.py "cozy coffee shop corner" --output eval/output/coffee.png
    uv run eval/generate.py "scene" --simple
    uv run eval/generate.py "scene" --endpoint http://localhost:8000/v1
    uv run eval/generate.py "scene" --endpoint http://localhost:8000/v1 --model lora
"""

import argparse
import ast
import os
import shutil
import subprocess
import sys
from pathlib import Path

import anthropic
import openai
from dotenv import load_dotenv

HERE = Path(__file__).parent.resolve()

load_dotenv(HERE.parent / ".env")

SYSTEM_PROMPT = """\
You are an expert Blender 5.1 Python (bpy) scripter. Given a scene description, \
output a single self-contained Python script. Follow these rules exactly:

1. Imports: bpy, math, os, sys ONLY. No third-party libraries.
2. Parse --output <path> from sys.argv after the "--" separator.
3. Clear the scene: bpy.ops.object.select_all(action="SELECT") then bpy.ops.object.delete().
4. Build the scene using ONLY Blender primitives: cube, plane, cylinder, sphere, cone.
   No external assets, no imports of .blend files.
5. Materials — CRITICAL rules for Blender 5.1:
   - Always set mat.use_nodes = True
   - Access Principled BSDF via mat.node_tree.nodes["Principled BSDF"]
   - Only set "Base Color", "Roughness", "Metallic" inputs. DO NOT use "Emission" \
— it does not exist as a direct input in Blender 5.1. Use an Emission shader node \
separately if you need emission.
   - Never use mat.diffuse_color.
6. Lighting: add at least one AREA light (bpy.ops.object.light_add(type="AREA")).
7. Camera: position with a clear isometric-ish view, set bpy.context.scene.camera.
8. Render setup:
   - scene.render.engine = "BLENDER_EEVEE"
   - resolution 800x600, PNG format
   - scene.render.filepath = os.path.abspath(output_path)
   - World background: access bpy.data.worlds["World"].node_tree, \
     set the Background node color (do NOT call world.use_nodes = True).
9. Call bpy.ops.render.render(write_still=True).
10. Wrap in main(), call main() at bottom.
11. Keep the script concise — aim for under 200 lines. Use helper functions.

Output ONLY the raw Python code. No markdown fences. No explanation."""

BLENDER_SCRIPT_HEADER = """\
# AUTO-GENERATED by generate.py — do not edit manually
# Prompt: {prompt}
"""


def strip_fences(code: str) -> str:
    if code.startswith("```"):
        lines = code.splitlines()
        code = "\n".join(lines[1:-1] if lines[-1].strip() == "```" else lines[1:])
    return code.strip()


def _call_anthropic(messages: list, model: str, client: anthropic.Anthropic) -> str:
    # Anthropic keeps system prompt separate; pop it from messages if present
    sys_msgs = [m for m in messages if m["role"] == "system"]
    chat_msgs = [m for m in messages if m["role"] != "system"]
    system = sys_msgs[0]["content"] if sys_msgs else SYSTEM_PROMPT
    response = client.messages.create(
        model=model,
        max_tokens=8192,
        system=system,
        messages=chat_msgs,
    )
    return response.content[0].text


def _call_openai_compat(messages: list, model: str, client: openai.OpenAI) -> str:
    all_messages = [{"role": "system", "content": SYSTEM_PROMPT}] + messages
    response = client.chat.completions.create(
        model=model,
        max_tokens=8192,
        messages=all_messages,
    )
    return response.choices[0].message.content


def call_llm(
    prompt: str,
    client: anthropic.Anthropic | openai.OpenAI,
    model: str,
    max_retries: int = 2,
    extra_context: str | None = None,
) -> str:
    user_content = f"Scene description: {prompt}"
    if extra_context:
        user_content += f"\n\n{extra_context}"
    messages = [{"role": "user", "content": user_content}]

    for attempt in range(1, max_retries + 1):
        print(f"[generate] Calling {model} (attempt {attempt}) for: {prompt!r}")
        if isinstance(client, anthropic.Anthropic):
            raw = _call_anthropic(messages, model, client)
        else:
            raw = _call_openai_compat(messages, model, client)
        code = strip_fences(raw)

        try:
            ast.parse(code)
            return code
        except SyntaxError as e:
            print(f"[generate] Syntax error on attempt {attempt}: {e}")
            if attempt < max_retries:
                messages.append({"role": "assistant", "content": raw})
                messages.append({
                    "role": "user",
                    "content": (
                        f"The script has a Python syntax error: {e}. "
                        "Output the complete corrected script with no truncation."
                    ),
                })
            else:
                raise SyntaxError(f"Failed to generate valid Python after {max_retries} attempts: {e}") from e
    raise RuntimeError("Unreachable")


def save_script(code: str, prompt: str, out_dir: Path) -> Path:
    out_dir.mkdir(parents=True, exist_ok=True)
    slug = prompt[:40].lower().replace(" ", "_").replace("/", "-")
    script_path = out_dir / f"{slug}.py"
    header = BLENDER_SCRIPT_HEADER.format(prompt=prompt)
    script_path.write_text(header + code)
    print(f"[generate] Scene script saved to {script_path}")
    return script_path


def validate_syntax(script_path: Path) -> None:
    """Raise SyntaxError if the generated script has invalid Python syntax."""
    source = script_path.read_text()
    try:
        ast.parse(source)
    except SyntaxError as e:
        raise SyntaxError(f"Generated script has a syntax error: {e}") from e


def render(script_path: Path, render_output: Path) -> tuple[bool, str]:
    """Run Blender headlessly. Returns (success, blender_output)."""
    render_output.parent.mkdir(parents=True, exist_ok=True)
    cmd = [
        "blender",
        "--background",
        "--python", str(script_path),
        "--",
        "--output", str(render_output),
    ]
    print(f"[generate] Running: {' '.join(cmd)}")
    result = subprocess.run(cmd, capture_output=True, text=True)
    # Print Blender output so the user can follow along
    if result.stdout:
        print(result.stdout, end="")
    if result.stderr:
        print(result.stderr, end="", file=sys.stderr)

    combined = result.stdout + result.stderr
    success = render_output.exists()
    return success, combined


def _require_blender() -> None:
    if not shutil.which("blender"):
        print(
            "ERROR: 'blender' not found in PATH. Install Blender and ensure it's on PATH.",
            file=sys.stderr,
        )
        sys.exit(1)


def run_trajectory_mode(args: argparse.Namespace) -> None:
    """Single multi-turn trajectory with BlenderEnvironment (tools: write_file, render)."""
    # These modules live in data/ (root project env); add to path lazily.
    sys.path.insert(0, str(HERE.parent / "data"))
    from generate_trajectories import run_trajectory
    from trajectory import PromptEntry

    _require_blender()
    api_key = os.getenv("ANTHROPIC_API_KEY")
    if not api_key:
        print("ERROR: ANTHROPIC_API_KEY not set in environment or .env", file=sys.stderr)
        sys.exit(1)

    model = args.model or "claude-sonnet-4-6"
    client = anthropic.Anthropic(api_key=api_key)
    artifacts_dir = (
        Path(args.artifacts_dir)
        if args.artifacts_dir
        else HERE / "trajectory_artifacts"
    )

    entry = PromptEntry(prompt=args.prompt)
    traj = run_trajectory(
        prompt_entry=entry,
        client=client,
        model=model,
        artifacts_dir=artifacts_dir,
        max_turns=args.max_turns,
        thinking_budget=args.thinking_budget,
        verbose=True,
        rate_limiter=None,
    )

    if traj.final_image_path:
        print(f"[generate] Final render: {traj.final_image_path}")
    if args.output:
        out = Path(args.output)
        out.parent.mkdir(parents=True, exist_ok=True)
        if traj.final_image_path and Path(traj.final_image_path).exists():
            shutil.copy2(traj.final_image_path, out)
            print(f"[generate] Copied to {out.resolve()}")
        else:
            print(
                "[generate] No successful render to copy; --output not written.",
                file=sys.stderr,
            )

    sys.exit(0 if traj.success else 1)


def simple_mode(args: argparse.Namespace) -> None:
    """One-shot LLM code generation + local render / retry (legacy)."""
    _require_blender()

    if args.endpoint:
        client: anthropic.Anthropic | openai.OpenAI = openai.OpenAI(
            base_url=args.endpoint,
            api_key=os.getenv("OPENAI_API_KEY", "none"),
        )
        if args.model:
            model = args.model
        else:
            models = client.models.list()
            available = [m.id for m in models.data]
            if not available:
                print("ERROR: no models found at endpoint", file=sys.stderr)
                sys.exit(1)
            model = available[0]
            print(f"[generate] Auto-detected model: {model}")
        print(f"[generate] Using endpoint {args.endpoint} with model {model}")
    else:
        api_key = os.getenv("ANTHROPIC_API_KEY")
        if not api_key:
            print("ERROR: ANTHROPIC_API_KEY not set in environment or .env", file=sys.stderr)
            sys.exit(1)
        model = args.model or "claude-sonnet-4-6"
        client = anthropic.Anthropic(api_key=api_key)

    code = call_llm(args.prompt, client, model)
    scripts_dir = Path(args.scripts_dir) if args.scripts_dir else HERE / "scripts"
    script_path = save_script(code, args.prompt, scripts_dir)
    print("[generate] Syntax OK.")

    if args.output:
        render_output = Path(args.output)
    else:
        slug = args.prompt[:40].lower().replace(" ", "_").replace("/", "-")
        render_output = HERE / "output" / f"{slug}.png"

    max_render_retries = 2
    for attempt in range(1, max_render_retries + 1):
        success, blender_output = render(script_path, render_output)
        if success:
            print(f"[generate] Done. Output: {render_output}")
            break

        print(f"[generate] Render failed (attempt {attempt}).", file=sys.stderr)
        if attempt < max_render_retries:
            error_lines = [
                l for l in blender_output.splitlines()
                if any(
                    k in l
                    for k in (
                        "Error",
                        "Traceback",
                        "line ",
                        "KeyError",
                        "AttributeError",
                        "TypeError",
                    )
                )
            ]
            error_summary = "\n".join(error_lines[-20:])
            print("[generate] Asking LLM to fix runtime error...", file=sys.stderr)
            fix_code = call_llm(
                args.prompt,
                client,
                model,
                extra_context=(
                    f"The previous script failed at runtime with this Blender error:\n{error_summary}\n"
                    "Fix the issue and output the complete corrected script."
                ),
            )
            script_path = save_script(fix_code, args.prompt + "_fix", scripts_dir)
            print(f"[generate] Fixed script saved to {script_path}")
        else:
            print(
                "[generate] ERROR: Render failed after retries. See Blender output above.",
                file=sys.stderr,
            )
            sys.exit(1)


def build_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(description="Text prompt → Blender render")
    parser.add_argument("prompt", help='Scene description, e.g. "high school study room"')
    parser.add_argument(
        "--output",
        default=None,
        help="Output PNG path (default: <script_dir>/output/<slug>.png in simple mode)",
    )
    parser.add_argument(
        "--scripts-dir",
        default=None,
        help="Directory to save generated .py scene scripts (simple mode only)",
    )
    parser.add_argument(
        "--endpoint",
        default=None,
        help="OpenAI-compatible base URL. Implies --simple (tools API not available).",
    )
    parser.add_argument(
        "--model",
        default=None,
        help="Model name. Defaults to claude-sonnet-4-6 (Anthropic). "
        "With --endpoint, auto-detected from /models if omitted.",
    )
    parser.add_argument(
        "--simple",
        action="store_true",
        help="Legacy: single-shot code generation and render retries (no tools).",
    )
    parser.add_argument(
        "--artifacts-dir",
        default=None,
        help="Parent directory for per-run work dirs (trajectory mode). "
        f"Default: {HERE / 'trajectory_artifacts'}",
    )
    parser.add_argument(
        "--max-turns",
        type=int,
        default=8,
        help="Max agent turns (trajectory mode).",
    )
    parser.add_argument(
        "--thinking-budget",
        type=int,
        default=8000,
        help="Extended thinking token budget (trajectory mode; 0 to disable).",
    )
    return parser


def main() -> None:
    args = build_parser().parse_args()

    use_trajectory = not args.endpoint and not args.simple
    if use_trajectory:
        run_trajectory_mode(args)
    else:
        simple_mode(args)


if __name__ == "__main__":
    main()
